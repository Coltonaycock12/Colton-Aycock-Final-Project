using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using Colton_Aycock_Final_Project.Models;

namespace Colton_Aycock_Final_Project.Pages.Products
{
    public class DetailsModel : PageModel
    {
        private readonly Colton_Aycock_Final_Project.Models.ProductDbContext _context;

        public DetailsModel(Colton_Aycock_Final_Project.Models.ProductDbContext context)
        {
            _context = context;
        }

      public Product Product { get; set; } = default!; 

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null || _context.Products == null)
            {
                return NotFound();
            }

            var product = await _context.Products.FirstOrDefaultAsync(m => m.ProductID == id);
            if (product == null)
            {
                return NotFound();
            }
            else 
            {
                Product = product;
            }
            return Page();
        }
    }
}
